#include "libft.h"

char	*ft_strmapi(const char *s, char (*f)(unsigned int, char))
{
	char	*result;
	int		i;
	int		len;

	i = 0;
	len = ft_strlen(s);
	if (!s)
		return (NULL);
	result = (char *)malloc(len * sizeof(char) + 1);
	if (!result)
		return (NULL);
	while (s[i])
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
